function ArticleCard({ article }: { article: Article }) {
  const [openFaq, setOpenFaq] = useState<number | null>(null)

  const scrollToContact = () => {
    const el = document.querySelector('#contact')
    if (el) el.scrollIntoView({ behavior: 'smooth' })
  }

  return (
    <article
      id={article.id}
      className="rounded-3xl overflow-hidden mb-12 scroll-mt-28"
      style={{
        background: 'rgba(255, 255, 255, 0.02)',
        border: '1px solid rgba(255, 255, 255, 0.06)',
      }}
    >
      <div className="p-6 sm:p-8">
        <h3
          className="text-xl sm:text-2xl lg:text-3xl font-bold mb-4"
          style={{
            fontFamily: "'Playfair Display', serif",
            color: '#FFFFFF',
          }}
        >
          {article.h2}
        </h3>

        {/* Short definition */}
        <p
          className="text-sm sm:text-base leading-relaxed mb-6 pb-6"
          style={{
            color: '#C5C6C7',
            borderBottom: '1px solid rgba(0, 191, 255, 0.1)',
          }}
        >
          {article.shortDef}
        </p>

        {/* Full text paragraphs */}
        <div className="space-y-4 mb-8">
          {article.paragraphs.map((para, i) => (
            <p
              key={i}
              className="text-sm leading-relaxed"
              style={{ color: '#aaa' }}
            >
              {para}
            </p>
          ))}
        </div>

        {/* FAQ */}
        <div className="mb-8">
          <h4
            className="text-sm font-semibold uppercase tracking-wide mb-4"
            style={{ color: '#00BFFF' }}
          >
            FAQ
          </h4>
          <div className="space-y-2">
            {article.faq.map((f, i) => (
              <div
                key={i}
                className="rounded-xl overflow-hidden"
                style={{
                  background: 'rgba(0, 191, 255, 0.03)',
                  border: '1px solid rgba(0, 191, 255, 0.08)',
                }}
              >
                <button
                  onClick={() => setOpenFaq(openFaq === i ? null : i)}
                  className="w-full flex items-center justify-between px-4 py-3 text-left"
                >
                  <span
                    className="text-sm font-medium"
                    style={{ color: '#C5C6C7' }}
                  >
                    {f.q}
                  </span>
                  <svg
                    className={`w-4 h-4 flex-shrink-0 ml-3 transition-transform ${
                      openFaq === i ? 'rotate-180' : ''
                    }`}
                    style={{ color: '#00BFFF' }}
                    fill="none"
                    stroke="currentColor"
                    strokeWidth="2"
                    viewBox="0 0 24 24"
                  >
                    <path
                      strokeLinecap="round"
                      strokeLinejoin="round"
                      d="M19.5 8.25l-7.5 7.5-7.5-7.5"
                    />
                  </svg>
                </button>
                {openFaq === i && (
                  <div
                    className="px-4 pb-3 text-sm leading-relaxed"
                    style={{ color: '#888' }}
                  >
                    {f.a}
                  </div>
                )}
              </div>
            ))}
          </div>
        </div>

        {/* CTA */}
        <button
          onClick={scrollToContact}
          className="abi-btn-primary text-xs py-3 px-6"
        >
          {article.cta}
        </button>
      </div>
    </article>
  )
}

